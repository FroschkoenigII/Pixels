package pixels.testReader.reader;

public class testResultReaderException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -703467595659749565L;

	public testResultReaderException() {
		super();
	}

	public testResultReaderException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public testResultReaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public testResultReaderException(String message) {
		super(message);
	}

	public testResultReaderException(Throwable cause) {
		super(cause);
	}
	
	

}
